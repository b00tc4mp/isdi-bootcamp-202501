// removes the last element from an array
// return deleted element

const myFish = ['angel','clown','mandarin','sturgeon'];

console.log(myFish.pop());
// Expected output: 'sturgeon'

console.log(myFish);
// Expected output: Array ['angel','clown','mandarin']

myFish.pop();

console.log(myFish);
// Expected output: Array ['angel','clown']